package cn.edu.scau.zxks.entity;

public class ExamMajorSite {
	private String majorId;
	
	private String majorName;
	
	private String examName;
	
	private String siteId;
	
	private String siteName;
	
	private int num;
	
    private String siteLinkman;

    private String siteLinkphone;

	public String getMajorId() {
		return majorId;
	}

	public void setMajorId(String majorId) {
		this.majorId = majorId;
	}

	public String getMajorName() {
		return majorName;
	}

	public void setMajorName(String majorName) {
		this.majorName = majorName;
	}

	public String getExamName() {
		return examName;
	}

	public void setExamName(String examName) {
		this.examName = examName;
	}

	public String getSiteId() {
		return siteId;
	}

	public void setSiteId(String siteId) {
		this.siteId = siteId;
	}

	public String getSiteName() {
		return siteName;
	}

	public void setSiteName(String siteName) {
		this.siteName = siteName;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getSiteLinkman() {
		return siteLinkman;
	}

	public void setSiteLinkman(String siteLinkman) {
		this.siteLinkman = siteLinkman;
	}

	public String getSiteLinkphone() {
		return siteLinkphone;
	}

	public void setSiteLinkphone(String siteLinkphone) {
		this.siteLinkphone = siteLinkphone;
	}

	@Override
	public String toString() {
		return "ExamMajorSite [majorId=" + majorId + ", majorName=" + majorName
				+ ", examName=" + examName + ", siteId=" + siteId
				+ ", siteName=" + siteName + ", num=" + num + ", siteLinkman="
				+ siteLinkman + ", siteLinkphone=" + siteLinkphone + "]";
	}

	public ExamMajorSite(String majorId, String majorName, String examName,
			String siteId, String siteName, int num, String siteLinkman,
			String siteLinkphone) {
		super();
		this.majorId = majorId;
		this.majorName = majorName;
		this.examName = examName;
		this.siteId = siteId;
		this.siteName = siteName;
		this.num = num;
		this.siteLinkman = siteLinkman;
		this.siteLinkphone = siteLinkphone;
	}

	public ExamMajorSite() {
		super();
		// TODO Auto-generated constructor stub
	}


}
